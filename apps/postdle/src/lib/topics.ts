/**
 * 오늘의 화두 — 날짜 기준 결정적 선택.
 * 하루 안에서는 고정, 매일 하나씩 순환(연중 일수 modulo 길이).
 */
export function pickDailyTopic(seedDate: Date = new Date()): string {
  const start = new Date(seedDate.getFullYear(), 0, 0);
  const day = Math.floor((seedDate.getTime() - start.getTime()) / 86400000);
  return EVERGREEN_TOPICS[((day % EVERGREEN_TOPICS.length) + EVERGREEN_TOPICS.length) % EVERGREEN_TOPICS.length];
}
