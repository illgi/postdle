/** 오늘의 문장 (날짜 기준 결정적 선택) */
export function pickTodayQuote(d: Date = new Date()): Quote {
  const start = new Date(d.getFullYear(), 0, 0);
  const day = Math.floor((d.getTime() - start.getTime()) / 86400000);
  return QUOTES[day % QUOTES.length];
}

export function getExampleById(id: string): FeedItem | undefined {
  return EXAMPLE_POSTS.find((e) => e.page.id === id);
}

export function isExampleId(id: string): boolean {
  return id.startsWith('ex-');
}
