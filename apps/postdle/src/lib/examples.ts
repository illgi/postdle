import type { FeedItem } from './pagedle';

function mk(id: string, memberName: string, pageName: string, date: string, content: string): FeedItem {
  return {
    page: {
      id,
      content,
      type: 'MARKDOWN',
      pageName,
      category: 'postdle',
      memberId: '',
      createTime: date,
      updateTime: date,
    },
    memberName,
  };
}

